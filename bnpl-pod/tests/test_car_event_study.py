"""
Phase B.2 tests --- CAR event-study module.

Exercises `backtest/car_event_study.py` on deterministic synthetic returns
so that:
    * `market_model` recovers injected alpha and betas within tolerance.
    * `abnormal_returns` is a clean residual on the estimation window.
    * `car` sums the AR vector.
    * `aar_across_events` and `bootstrap_car_null` behave predictably under
      a known-zero-mean null.

Warehouse never touched. All fixtures are in-memory.
"""
